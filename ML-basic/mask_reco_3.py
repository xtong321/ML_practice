import cv2
import numpy as np

device = torch.device("cuda:0" if torch.cuda.is_available() else "cpu")
cap = cv2.VideoCapture(0)

while True:
    ret, frame = cap.read()
    frame = cv2.resize(frame, (0, 0), fx=0.5, fy=0.5)

    img = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)
    img = cv2.resize(img, (224, 224))
    img_ = torch.from_numpy(img).float().permute(2, 0, 1).unsqueeze(0)

    img_ = img_.to(device)
    outputs = model_ft(img_)
    
    # 输出概率最大的类别
    labels = ["BG","MASK","NOMASK"]
    _, index = torch.max(outputs, 1)
    percentage = torch.nn.functional.softmax(outputs, dim=1)[0] * 100
    print(labels[index[0]], percentage[index[0]].item())
    mclass = labels[index[0]]

    cv2.putText(frame, mclass, (10, 20),cv2.FONT_HERSHEY_SIMPLEX, 1, (0,0,255), 2)
    cv2.imshow("frame", frame)
    mKey = cv2.waitKey(1) & 0xFF
    if mKey == ord('q'):
        break
    